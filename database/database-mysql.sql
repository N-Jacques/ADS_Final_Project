CREATE TABLE colleges ( -- This will store info about colleges.
  college_id        VARCHAR(10)     NOT NULL
  , college_name    VARCHAR(100)    DEFAULT ' '
  , CONSTRAINT college_id_pk PRIMARY KEY(college_id)
);

CREATE TABLE programs ( -- This will store info about programs under colleges.
  program_id        VARCHAR(10)     NOT NULL
  , program_name    VARCHAR(100)    DEFAULT ' '
  , college_id      VARCHAR(10)     NOT NULL
  , CONSTRAINT program_id_pk PRIMARY KEY(program_id)
  , CONSTRAINT college_id_fk FOREIGN KEY(college_id) REFERENCES colleges(college_id)
);

CREATE TABLE status ( -- This will store info about student status if irregular or not.
  status_id         VARCHAR(3)      NOT NULL
  , status_name     VARCHAR(10)     DEFAULT ' '
  , CONSTRAINT status_id_pk PRIMARY KEY(status_id)
);

CREATE TABLE students ( -- This will store info about the student.
  student_id        VARCHAR(10)     NOT NULL
  , firstname       VARCHAR(50)     DEFAULT ' '
  , middlename      VARCHAR(50)     DEFAULT ' '
  , lastname        VARCHAR(50)     DEFAULT ' '
  , yrlevel         INTEGER         DEFAULT 0
  , program_id      VARCHAR(10)     NOT NULL
  , status_id       VARCHAR(3)      NOT NULL
  , CONSTRAINT student_id_pk PRIMARY KEY(student_id)
  , CONSTRAINT program_id_fk FOREIGN KEY(program_id) REFERENCES programs(program_id)
  , CONSTRAINT status_id_fk FOREIGN KEY(status_id) REFERENCES status(status_id)
);

CREATE TABLE credentials (   -- Can be added if the login page requires password
  login_id          VARCHAR(5)      NOT NULL
  , student_id      VARCHAR(10)     NOT NULL
  , username        VARCHAR(20)     DEFAULT ' '
  , passkey         VARCHAR(20)     DEFAULT ' '
  , CONSTRAINT login_id_pk PRIMARY KEY(login_id)
  , CONSTRAINT student_id_fk1 FOREIGN KEY(student_id) REFERENCES students(student_id)
);

CREATE TABLE semester ( -- This will store info about semesters and their durations. MM/DD/YYYY is the format. Change it based on your device if needed.
  sem_id            VARCHAR(5)      NOT NULL
  , date_start      DATE            DEFAULT '1990-01-01' 
  , date_end        DATE            DEFAULT '1990-01-01'
  , CONSTRAINT sem_id_pk PRIMARY KEY(sem_id)
);

CREATE TABLE subject_type ( -- This will store info about semesters and their durations.
  subtype_id        VARCHAR(3)      NOT NULL
  , subtype_desc    VARCHAR(5)      DEFAULT ' '
  , CONSTRAINT subtype_id_pk PRIMARY KEY(subtype_id)
);

CREATE TABLE subjects ( -- This will store info about subjects and whether they're minors or majors.
  sub_code          VARCHAR(15)     NOT NULL
  , title           VARCHAR(100)    DEFAULT ' '
  , units           INTEGER         DEFAULT 0
  , subtype_id      VARCHAR(3)      NOT NULL
  , CONSTRAINT sub_code_pk PRIMARY KEY(sub_code)
  , CONSTRAINT subtype_id_fk FOREIGN KEY(subtype_id) REFERENCES subject_type(subtype_id)
);

CREATE TABLE subjects_taken ( -- This will store info about subjects already taken by the students.
  student_id        VARCHAR(10)     NOT NULL
  , sub_code        VARCHAR(15)     NOT NULL
  , sem_id          VARCHAR(5)      NOT NULL
  , grade           DECIMAL(10,2)   DEFAULT 0.00
  , CONSTRAINT subjects_taken_pk PRIMARY KEY(student_id, sub_code, sem_id)
  , CONSTRAINT student_id_fk2 FOREIGN KEY(student_id) REFERENCES students(student_id)
  , CONSTRAINT sub_code_fk1 FOREIGN KEY(sub_code) REFERENCES subjects(sub_code)
  , CONSTRAINT sem_id_fk1 FOREIGN KEY(sem_id) REFERENCES semester(sem_id)
);

CREATE TABLE day_details ( -- This will store info about days of the week.
  day_id            VARCHAR(2)      NOT NULL
  , day_name        VARCHAR(10)    DEFAULT ' '
  , CONSTRAINT day_id_pk PRIMARY KEY(day_id)
);

CREATE TABLE schedule ( -- This will store info about available schedules for the subjects. Different sections with different days and times. Also, different semester ids to distinguish subjects offered each sem.
  sub_code          VARCHAR(15)     NOT NULL
  , section         VARCHAR(2)      DEFAULT ' '
  , sem_id          VARCHAR(5)      NOT NULL
  , day_id          VARCHAR(2)      NOT NULL
  , time_start      VARCHAR(5)      DEFAULT '00:00'
  , time_end        VARCHAR(5)      DEFAULT '00:00'
  , room            VARCHAR(10)     DEFAULT ' '
  , slots           INTEGER         DEFAULT 0
  , CONSTRAINT schedule_pk PRIMARY KEY(sub_code, section, sem_id, day_id, time_start, time_end)
  , CONSTRAINT sub_code_fk2 FOREIGN KEY(sub_code) REFERENCES subjects(sub_code)
  , CONSTRAINT sem_id_fk2 FOREIGN KEY(sem_id) REFERENCES semester(sem_id)
  , CONSTRAINT day_id_fk FOREIGN KEY(day_id) REFERENCES day_details(day_id)
);

CREATE TABLE enlistment ( -- This will store info about the students' enlistments.
  enlistment_id     VARCHAR(5)      NOT NULL
  , student_id      VARCHAR(10)     NOT NULL
  , sem_id          VARCHAR(5)      NOT NULL
  , date_created    DATE            DEFAULT '1990-01-01' 
  , CONSTRAINT enlistment_id_pk1 PRIMARY KEY(enlistment_id)
  , CONSTRAINT student_id_fk3 FOREIGN KEY(student_id) REFERENCES students(student_id)
  , CONSTRAINT sem_id_fk3 FOREIGN KEY(sem_id) REFERENCES semester(sem_id)
);

CREATE TABLE enlisted_subjects ( -- This will track the subjects being enlisted.
  enlistment_id     VARCHAR(5)      NOT NULL
  , sub_code        VARCHAR(15)     NOT NULL
  , CONSTRAINT enlisted_subjects_pk PRIMARY KEY(enlistment_id, sub_code)
  , CONSTRAINT enlistment_id_fk1 FOREIGN KEY(enlistment_id) REFERENCES enlistment(enlistment_id)
  , CONSTRAINT sub_code_fk3 FOREIGN KEY(sub_code) REFERENCES subjects(sub_code)
); 

CREATE TABLE enrolled ( -- This will keep track the students that are already enrolled.
  enlistment_id     VARCHAR(5)      NOT NULL
  , date_created    TIMESTAMP       DEFAULT '1990-01-01'
  , CONSTRAINT enlistment_id_pk2 PRIMARY KEY(enlistment_id)
  , CONSTRAINT enlistment_id_fk2 FOREIGN KEY(enlistment_id) REFERENCES enlistment(enlistment_id)
);

